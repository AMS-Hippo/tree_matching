@dataclass
class MCMCResult:
    best_master: np.ndarray
    best_score: float
    trace_best_scores: List[float]
    accept_rate: float


def _default_length_prior_logp(L: int, lam: float = 0.0) -> float:
    """A simple exponential length prior: log P(L) ∝ -lam * L."""
    return -lam * float(L)


class MasterSequenceEstimator:
    """Estimate master sequence (and optionally its length) under IndelLikelihood."""

    def __init__(
        self,
        alphabet: Alphabet,
        sequences: Sequence[TokenSeq],
        params: Union[IndelParams, IndelModelParams],
        length_prior_lambda: float = 0.0,
        seed: int = 0,
    ):
        self.alphabet = alphabet
        # Allow either IndelParams (core) or IndelModelParams (user-facing).
        if isinstance(params, IndelModelParams):
            params_core = params.to_core()
        else:
            params_core = params
        self.params = params_core
        self.lik = IndelLikelihood(params_core)
        self.rng = np.random.default_rng(seed)
        self.length_prior_lambda = float(length_prior_lambda)

        # Encode observations once.
        self.obs = [alphabet.encode(s) for s in sequences]

    def loglik_all(self, master: np.ndarray) -> float:
        master = np.asarray(master, dtype=np.int32)
        total = 0.0
        for o in self.obs:
            ll = self.lik.forward_loglik(master, o)
            if not np.isfinite(ll):
                return -math.inf
            total += ll
        return float(total)

    def logposterior(self, master: np.ndarray) -> float:
        master = np.asarray(master, dtype=np.int32)
        return self.loglik_all(master) + _default_length_prior_logp(int(master.size), self.length_prior_lambda)

    # ---- initializers ----

    def init_from_shortest(self) -> np.ndarray:
        """Initialize master as the shortest observed sequence (a crude but often decent start)."""
        idx = int(np.argmin([o.size for o in self.obs]))
        return np.array(self.obs[idx], copy=True)

    def init_random(self, length: int) -> np.ndarray:
        return self.rng.integers(0, self.alphabet.size, size=int(length), dtype=np.int32)

    # ---- MCMC proposals ----

    def _available_move_weights(self, fixed_length: bool) -> Dict[str, float]:
        w = {'sub': 1.0}
        if not fixed_length:
            w['ins'] = 0.5
            w['del'] = 0.5
        return w

    def propose(self, master: np.ndarray, fixed_length: bool = False) -> Tuple[np.ndarray, float]:
        """Propose a new master sequence.

        Returns (proposal, log_q_reverse_minus_log_q_forward).
        """
        master = np.asarray(master, dtype=np.int32)
        L = int(master.size)
        A = int(self.alphabet.size)

        weights = self._available_move_weights(fixed_length=fixed_length)
        keys = list(weights.keys())
        probs = np.array([weights[k] for k in keys], dtype=np.float64)
        probs = probs / probs.sum()

        move = str(self.rng.choice(keys, p=probs))

        def p_move_for(keys_here: List[str], move_name: str) -> float:
            # recompute normalized weights in a context
            w = np.array([weights[k] for k in keys_here], dtype=np.float64)
            w = w / w.sum()
            return float(w[keys_here.index(move_name)])

        if move == 'sub':
            if L == 0:
                # fallback to insertion if allowed
                if fixed_length:
                    return master.copy(), 0.0
                move = 'ins'
            else:
                pos = int(self.rng.integers(0, L))
                old = int(master[pos])
                new_sym = int(self.rng.integers(0, A - 1))
                if new_sym >= old:
                    new_sym += 1
                prop = master.copy()
                prop[pos] = new_sym
                # symmetric given pos chosen uniformly and new_sym chosen uniformly among A-1
                return prop, 0.0

        if move == 'ins':
            if fixed_length:
                return master.copy(), 0.0
            pos = int(self.rng.integers(0, L + 1))
            sym = int(self.rng.integers(0, A))
            prop = np.empty(L + 1, dtype=np.int32)
            prop[:pos] = master[:pos]
            prop[pos] = sym
            prop[pos + 1 :] = master[pos:]

            # Proposal ratio for reversible-jump insertion/deletion
            # q(forward) = P_ins(old) * 1/(L+1) * 1/A
            # q(reverse) = P_del(new) * 1/(L+1)
            keys_old = keys
            keys_new = keys
            p_ins_old = p_move_for(keys_old, 'ins')
            p_del_new = p_move_for(keys_new, 'del')
            log_ratio = math.log(p_del_new) - math.log(p_ins_old) + math.log(A)
            return prop, log_ratio

        if move == 'del':
            if fixed_length or L == 0:
                return master.copy(), 0.0
            pos = int(self.rng.integers(0, L))
            prop = np.concatenate([master[:pos], master[pos + 1 :]]).astype(np.int32, copy=False)

            # Reverse is insertion
            # q(forward) = P_del(old) * 1/L
            # q(reverse) = P_ins(new) * 1/L * 1/A
            keys_old = keys
            keys_new = keys
            p_del_old = p_move_for(keys_old, 'del')
            p_ins_new = p_move_for(keys_new, 'ins')
            log_ratio = math.log(p_ins_new) - math.log(p_del_old) - math.log(A)
            return prop, log_ratio

        return master.copy(), 0.0

    # ---- MCMC driver ----

    def run_mcmc(
        self,
        init_master: Optional[np.ndarray] = None,
        n_steps: int = 5000,
        burn_in: int = 500,
        thin: int = 10,
        fixed_length: bool = False,
        temperature: float = 1.0,
        progress: bool = True,
    ) -> MCMCResult:
        """Metropolis-Hastings (optionally annealed via temperature < 1).

        Returns the best master visited (maximum log posterior).
        """
        if init_master is None:
            cur = self.init_from_shortest()
        else:
            cur = np.asarray(init_master, dtype=np.int32).copy()

        cur_score = self.logposterior(cur)
        best = cur.copy()
        best_score = float(cur_score)

        acc = 0
        tried = 0
        trace_best: List[float] = []

        it = range(n_steps)
        if progress and _HAVE_TQDM:
            it = tqdm(it, desc="MCMC", leave=False)

        for t in it:
            prop, log_q_ratio = self.propose(cur, fixed_length=fixed_length)
            prop_score = self.logposterior(prop)

            tried += 1
            if np.isfinite(prop_score) and np.isfinite(cur_score):
                log_alpha = (prop_score - cur_score) / float(max(temperature, 1e-12)) + float(log_q_ratio)
                if math.log(self.rng.random()) < min(0.0, log_alpha):
                    cur = prop
                    cur_score = prop_score
                    acc += 1

            if float(cur_score) > best_score:
                best = cur.copy()
                best_score = float(cur_score)

            if t >= burn_in and (t - burn_in) % max(thin, 1) == 0:
                trace_best.append(best_score)

        return MCMCResult(
            best_master=best,
            best_score=best_score,
            trace_best_scores=trace_best,
            accept_rate=(acc / tried) if tried else 0.0,
        )


