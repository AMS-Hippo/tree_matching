"""master_sequence_indel.py

Inference for a shared "master" symbol sequence under an insertion/deletion model.

Problem setting
---------------
You observe many sequences over a discrete alphabet (e.g., extracted from pairs of graphs).
Each observed sequence is assumed to arise from a single unknown master sequence via:
  1) Deletions of master elements (each master symbol is independently deleted w.p. p_del), and
  2) Insertions of background symbols (a geometric run of i.i.d. symbols from background
     distribution bg is inserted before each master symbol, and also at the end).

This file implements:
  - Background estimation (empirical + smoothing)
  - Likelihood of observed sequences given a master sequence (log-forward DP)
  - Optional Viterbi alignment
  - MCMC / simulated annealing search over master sequences (including variable length)
  - Simple grid search wrapper for (length, p_ins, p_del)

Dependencies: numpy (required). tqdm (optional).

Author: generated by ChatGPT
"""

