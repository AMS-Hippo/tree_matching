@dataclass(frozen=True)
class IndelParams:
    """Parameters for the insertion/deletion model."""

    p_ins: float  # probability of inserting a background symbol before processing next master symbol
    p_del: float  # probability of deleting a master symbol when it is processed
    bg_probs: np.ndarray  # shape (A,)
    eps_sub: float = 0.0  # substitution/noise rate for master emissions

    def __post_init__(self):
        if not (0.0 <= self.p_ins < 1.0):
            raise ValueError("p_ins must be in [0,1).")
        if not (0.0 <= self.p_del < 1.0):
            raise ValueError("p_del must be in [0,1).")
        if not (0.0 <= self.eps_sub <= 1.0):
            raise ValueError("eps_sub must be in [0,1].")
        bg = np.asarray(self.bg_probs, dtype=np.float64)
        if bg.ndim != 1 or bg.size == 0:
            raise ValueError("bg_probs must be a non-empty 1D array.")
        if np.any(bg < 0) or not np.isfinite(bg).all():
            raise ValueError("bg_probs must be finite and nonnegative.")
        s = float(bg.sum())
        if not (s > 0):
            raise ValueError("bg_probs must sum to a positive value.")


@dataclass(frozen=True)
class IndelModelParams:
    """User-facing parameters that also carry the Alphabet.

    This is a thin wrapper around :class:`IndelParams` used internally.
    """

    alphabet: Alphabet
    p_ins: float
    p_del: float
    bg_probs: np.ndarray
    eps_sub: float = 0.0

    def __post_init__(self):
        bg = np.asarray(self.bg_probs, dtype=np.float64)
        if bg.ndim != 1 or bg.size != self.alphabet.size:
            raise ValueError("bg_probs must be a 1D array of length alphabet.size")

    def to_core(self) -> "IndelParams":
        return IndelParams(
            p_ins=float(self.p_ins),
            p_del=float(self.p_del),
            bg_probs=np.asarray(self.bg_probs, dtype=np.float64),
            eps_sub=float(self.eps_sub),
        )


def _log1m(p: float) -> float:
    """Stable log(1-p) for p in [0,1)."""
    # Clip to avoid log(0) due to floating error.
    if p >= 1.0:
        return -math.inf
    if p <= 0.0:
        return 0.0
    return math.log1p(-p)


def _logaddexp(a: float, b: float) -> float:
    return float(np.logaddexp(a, b))


class IndelLikelihood:
    """Likelihood and alignment routines under the indel model."""

    def __init__(self, params: IndelParams):
        self.params = params
        bg = np.asarray(params.bg_probs, dtype=np.float64)
        self.log_bg = np.log(bg / bg.sum())
        self.log_p_ins = math.log(params.p_ins) if params.p_ins > 0 else -math.inf
        self.log_1m_p_ins = _log1m(params.p_ins)
        self.log_p_del = math.log(params.p_del) if params.p_del > 0 else -math.inf
        self.log_1m_p_del = _log1m(params.p_del)

    def emission_logp(self, master_sym: int, obs_sym: int) -> float:
        """log P(obs_sym | master_sym) with optional noise."""
        eps = self.params.eps_sub
        if eps <= 0.0:
            return 0.0 if master_sym == obs_sym else -math.inf
        # P = (1-eps) * 1_{equal} + eps * bg(obs)
        base = eps * math.exp(self.log_bg[obs_sym])
        if master_sym == obs_sym:
            return math.log((1.0 - eps) + base)
        return math.log(base)

    def forward_loglik(self, master: np.ndarray, obs: np.ndarray) -> float:
        """Compute log P(obs | master, params) via forward DP (O(L*M), O(M) memory).

        master: int array of shape (L,)
        obs:    int array of shape (M,)
        """
        master = np.asarray(master, dtype=np.int32)
        obs = np.asarray(obs, dtype=np.int32)
        L = int(master.size)
        M = int(obs.size)

        # DP row for fixed i: log prob of generating obs[:j] after consuming i master symbols.
        row = np.full(M + 1, -math.inf, dtype=np.float64)
        row[0] = 0.0

        for i in range(L + 1):
            # Close insertions on this row: allow any number of inserts before processing next master symbol.
            if self.params.p_ins > 0.0 and M > 0:
                for j in range(M):
                    cur = float(row[j])
                    if not np.isfinite(cur):
                        continue
                    cand = cur + self.log_p_ins + float(self.log_bg[obs[j]])
                    row[j + 1] = np.logaddexp(row[j + 1], cand)

            if i == L:
                break

            next_row = np.full(M + 1, -math.inf, dtype=np.float64)
            ms = int(master[i])
            for j in range(M + 1):
                cur = float(row[j])
                if not np.isfinite(cur):
                    continue

                # Delete master symbol (no emission)
                if np.isfinite(self.log_p_del):
                    cand = cur + self.log_1m_p_ins + self.log_p_del
                    next_row[j] = np.logaddexp(next_row[j], cand)

                # Emit (match/substitute) master symbol
                if j < M and np.isfinite(self.log_1m_p_del):
                    elp = self.emission_logp(ms, int(obs[j]))
                    if np.isfinite(elp):
                        cand = cur + self.log_1m_p_ins + self.log_1m_p_del + elp
                        next_row[j + 1] = np.logaddexp(next_row[j + 1], cand)

            row = next_row

        # Stop after finishing the tail insertion-run
        return float(row[M]) + float(self.log_1m_p_ins)

    def viterbi_alignment(self, master: np.ndarray, obs: np.ndarray) -> Tuple[float, List[Tuple[str, Optional[int], Optional[int]]]]:
        """Most likely alignment path (Viterbi) with backpointers.

        Returns (log_prob, path) where path is a list of steps:
          ('I', None, j)  : inserted obs[j]
          ('D', i, None)  : deleted master[i]
          ('M', i, j)     : emitted obs[j] from master[i]

        Note: O(L*M) memory.
        """
        master = np.asarray(master, dtype=np.int32)
        obs = np.asarray(obs, dtype=np.int32)
        L = int(master.size)
        M = int(obs.size)

        dp = np.full((L + 1, M + 1), -math.inf, dtype=np.float64)
        bp: List[List[Optional[Tuple[int, int, str]]]] = [[None] * (M + 1) for _ in range(L + 1)]
        dp[0, 0] = 0.0

        for i in range(L + 1):
            for j in range(M + 1):
                cur = float(dp[i, j])
                if not np.isfinite(cur):
                    continue

                # Insert
                if j < M and np.isfinite(self.log_p_ins):
                    cand = cur + self.log_p_ins + float(self.log_bg[obs[j]])
                    if cand > dp[i, j + 1]:
                        dp[i, j + 1] = cand
                        bp[i][j + 1] = (i, j, 'I')

                # Process master if any left
                if i < L:
                    # Delete
                    if np.isfinite(self.log_p_del):
                        cand = cur + self.log_1m_p_ins + self.log_p_del
                        if cand > dp[i + 1, j]:
                            dp[i + 1, j] = cand
                            bp[i + 1][j] = (i, j, 'D')

                    # Emit
                    if j < M and np.isfinite(self.log_1m_p_del):
                        elp = self.emission_logp(int(master[i]), int(obs[j]))
                        if np.isfinite(elp):
                            cand = cur + self.log_1m_p_ins + self.log_1m_p_del + elp
                            if cand > dp[i + 1, j + 1]:
                                dp[i + 1, j + 1] = cand
                                bp[i + 1][j + 1] = (i, j, 'M')

        # Include stop prob
        best = float(dp[L, M]) + float(self.log_1m_p_ins)

        # Backtrack
        path: List[Tuple[str, Optional[int], Optional[int]]] = []
        i, j = L, M
        while not (i == 0 and j == 0):
            prev = bp[i][j]
            if prev is None:
                # No path (should be rare unless parameters make generation impossible)
                break
            pi, pj, step = prev
            if step == 'I':
                path.append(('I', None, pj))
            elif step == 'D':
                path.append(('D', pi, None))
            else:
                path.append(('M', pi, pj))
            i, j = pi, pj
        path.reverse()
        return best, path


