def _to_tokens(x: TokenSeq) -> List[str]:
    """Convert input to a list of tokens.

    - If x is a string, treat it as a sequence of characters.
    - If x is a sequence/array, coerce each element to str.
    """
    if isinstance(x, str):
        return list(x)
    if isinstance(x, np.ndarray):
        return [str(t) for t in x.tolist()]
    return [str(t) for t in x]


@dataclass(frozen=True)
class Alphabet:
    """A finite alphabet with encode/decode maps."""

    tokens: Tuple[str, ...]
    token_to_id: Dict[str, int]

    @staticmethod
    def from_sequences(seqs: Sequence[TokenSeq]) -> "Alphabet":
        uniq: Dict[str, None] = {}
        for s in seqs:
            for t in _to_tokens(s):
                uniq.setdefault(t, None)
        tokens = tuple(sorted(uniq.keys()))
        token_to_id = {t: i for i, t in enumerate(tokens)}
        return Alphabet(tokens=tokens, token_to_id=token_to_id)


    @staticmethod
    def from_tokens(tokens: Sequence[str]) -> "Alphabet":
        """Build an Alphabet from an explicit ordered token list.

        The token order defines the integer encoding. Duplicate tokens are not allowed.
        """
        toks = [str(t) for t in tokens]
        if len(set(toks)) != len(toks):
            raise ValueError("Duplicate tokens in alphabet.")
        tokens_t = tuple(toks)
        token_to_id = {t: i for i, t in enumerate(tokens_t)}
        return Alphabet(tokens=tokens_t, token_to_id=token_to_id)

    @property
    def size(self) -> int:
        return len(self.tokens)

    def encode(self, seq: TokenSeq) -> np.ndarray:
        toks = _to_tokens(seq)
        out = np.empty(len(toks), dtype=np.int32)
        for i, t in enumerate(toks):
            if t not in self.token_to_id:
                raise KeyError(f"Token {t!r} not in alphabet.")
            out[i] = self.token_to_id[t]
        return out

    def decode(self, arr: np.ndarray) -> List[str]:
        arr = np.asarray(arr, dtype=np.int64)
        return [self.tokens[int(i)] for i in arr]


