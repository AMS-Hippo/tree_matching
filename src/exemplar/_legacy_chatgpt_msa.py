@dataclass
class WorkflowResult:
    """Result of :func:`infer_master_workflow`."""

    master_tokens: List[str]
    params: IndelParams
    score: float
    diagnostics: Dict[str, Any]


def _choose_init_master_from_data(obs: List[np.ndarray], target_len: int, rng: np.random.Generator) -> np.ndarray:
    """Pick an observed sequence close to target_len; fall back to random."""
    if not obs:
        return np.zeros(0, dtype=np.int32)
    lens = np.array([int(o.size) for o in obs], dtype=np.int32)
    # prefer sequences not longer than target_len (helps avoid long masters)
    candidates = np.where(lens <= int(target_len))[0]
    if candidates.size == 0:
        idx = int(np.argmin(np.abs(lens - int(target_len))))
        return np.array(obs[idx], copy=True)
    # pick among closest lengths (ties broken randomly)
    cand_lens = lens[candidates]
    best = np.min(np.abs(cand_lens - int(target_len)))
    close = candidates[np.where(np.abs(cand_lens - int(target_len)) == best)[0]]
    idx = int(rng.choice(close))
    return np.array(obs[idx], copy=True)


def _estimate_p_ins_from_lengths(mean_len: float, L_master: int, p_del: float) -> float:
    """Heuristic p_ins init from mean observed length.

    Using E[obs_len] \approx (1-p_del)*L + (L+1)*p_ins/(1-p_ins).
    Solve for p_ins based on a guessed p_del.
    """
    L = max(1, int(L_master))
    base = (1.0 - float(p_del)) * float(L)
    ins_mean = max(0.0, float(mean_len) - base)
    # ins_mean = (L+1)*p/(1-p)  =>  p = ins_mean / (ins_mean + L+1)
    p = ins_mean / (ins_mean + float(L + 1)) if (ins_mean + float(L + 1)) > 0 else 0.0
    return float(np.clip(p, 1e-4, 0.8))


def _estimate_eps_sub_grid(
    emitted_pairs: List[Tuple[int, int]],
    bg_probs: np.ndarray,
    eps_max: float = 0.30,
    n_grid: int = 201,
) -> float:
    """1D grid MLE for eps_sub using aligned emissions.

    For each emitted pair (m, o):
        P(o|m) = (1-eps)*1_{o=m} + eps*bg(o)
    """
    if not emitted_pairs:
        return 0.0
    bg = np.asarray(bg_probs, dtype=np.float64)
    bg = bg / bg.sum()

    # Precompute bg(o) and equality flags.
    os = np.array([o for (_, o) in emitted_pairs], dtype=np.int32)
    ms = np.array([m for (m, _) in emitted_pairs], dtype=np.int32)
    eq = (os == ms)
    bg_o = bg[os]

    # Evaluate log-likelihood across grid.
    grid = np.linspace(0.0, float(eps_max), int(n_grid))
    ll = np.empty(grid.size, dtype=np.float64)
    for i, eps in enumerate(grid):
        if eps <= 0.0:
            # only possible if all are equal
            ll[i] = 0.0 if bool(eq.all()) else -np.inf
            continue
        # For eq positions: log((1-eps) + eps*bg(o)); else: log(eps*bg(o))
        ll_eq = np.log((1.0 - eps) + eps * bg_o[eq]) if bool(eq.any()) else 0.0
        ll_neq = np.log(eps * bg_o[~eq]) if bool((~eq).any()) else 0.0
        ll[i] = (ll_eq.sum() if isinstance(ll_eq, np.ndarray) else float(ll_eq)) + (
            ll_neq.sum() if isinstance(ll_neq, np.ndarray) else float(ll_neq)
        )

    best = int(np.nanargmax(ll))
    return float(grid[best])


def _em_fixed_length(
    obs: List[np.ndarray],
    master: np.ndarray,
    params: IndelParams,
    n_iters: int = 3,
    estimate_bg_from_inserts: bool = True,
    estimate_eps_sub: bool = True,
    bg_pseudocount: float = 1e-2,
    eps_max: float = 0.30,
    progress: bool = True,
    # If provided, these parameters are held fixed during the EM loop.
    fixed_p_ins: Optional[float] = None,
    fixed_p_del: Optional[float] = None,
    fixed_eps_sub: Optional[float] = None,
    fixed_bg_probs: Optional[np.ndarray] = None,
) -> Tuple[np.ndarray, IndelParams, Dict[str, Any]]:
    """EM-like loop (Viterbi E-step, closed-form M-step) at a fixed master length.

    This is designed for your regime: many short sequences (length ~ 10-20).
    """
    rng = np.random.default_rng(0)
    master = np.asarray(master, dtype=np.int32).copy()
    L = int(master.size)
    A = int(params.bg_probs.size)

    hist: Dict[str, Any] = {'iters': []}

    for it in range(int(n_iters)):
        lik = IndelLikelihood(params)

        # Sufficient statistics
        ins_total = 0
        del_total = 0
        match_total = 0

        # For master symbol update
        sym_counts = np.zeros((L, A), dtype=np.int64)

        # For background update (from inferred insertions)
        ins_bg_counts = np.zeros(A, dtype=np.int64)

        # For eps_sub update
        emitted_pairs: List[Tuple[int, int]] = []

        iterator = range(len(obs))
        if progress and _HAVE_TQDM:
            iterator = tqdm(iterator, desc=f"EM iter {it+1}/{n_iters}", leave=False)

        for s_idx in iterator:
            o = obs[s_idx]
            _, path = lik.viterbi_alignment(master, o)

            for step, mi, oj in path:
                if step == 'I':
                    j = int(oj)  # type: ignore[arg-type]
                    ins_total += 1
                    if 0 <= j < int(o.size):
                        ins_bg_counts[int(o[j])] += 1
                elif step == 'D':
                    del_total += 1
                else:  # 'M'
                    i = int(mi)  # type: ignore[arg-type]
                    j = int(oj)  # type: ignore[arg-type]
                    match_total += 1
                    if 0 <= i < L and 0 <= j < int(o.size):
                        sym = int(o[j])
                        sym_counts[i, sym] += 1
                        emitted_pairs.append((int(master[i]), sym))

        N = max(1, int(len(obs)))

        # --- M-step: update master symbols by per-position majority vote ---
        new_master = master.copy()
        for i in range(L):
            if sym_counts[i].sum() > 0:
                new_master[i] = int(np.argmax(sym_counts[i]))
            else:
                # If a position is never emitted, keep it (or randomize slightly)
                if rng.random() < 0.05:
                    new_master[i] = int(rng.integers(0, A))

        master = new_master

        # --- M-step: update parameters ---
        # p_del MLE: del / (del + match) with mild smoothing
        if fixed_p_del is not None:
            p_del_hat = float(fixed_p_del)
        else:
            denom_dm = float(del_total + match_total)
            if denom_dm <= 0:
                p_del_hat = params.p_del
            else:
                p_del_hat = float((del_total + 0.5) / (denom_dm + 1.0))

        # p_ins MLE under geometric runs: ins / (ins + runs), where runs = N*(L+1)
        if fixed_p_ins is not None:
            p_ins_hat = float(fixed_p_ins)
        else:
            runs = float(N * (L + 1))
            p_ins_hat = float((ins_total + 0.5) / (ins_total + runs + 1.0))

        # Background update: use inferred insertions if requested; otherwise keep.
        if fixed_bg_probs is not None:
            bg_probs = np.asarray(fixed_bg_probs, dtype=np.float64)
        else:
            bg_probs = np.asarray(params.bg_probs, dtype=np.float64)
            if estimate_bg_from_inserts:
                counts = np.full(A, float(bg_pseudocount), dtype=np.float64) + ins_bg_counts.astype(np.float64)
                bg_probs = counts / counts.sum()

        # eps_sub update from emitted pairs (optional)
        if fixed_eps_sub is not None:
            eps_hat = float(fixed_eps_sub)
        else:
            eps_hat = float(params.eps_sub)
            if estimate_eps_sub:
                eps_hat = _estimate_eps_sub_grid(emitted_pairs, bg_probs=bg_probs, eps_max=float(eps_max))

        params = IndelParams(
            p_ins=float(np.clip(p_ins_hat, 1e-4, 0.95)),
            p_del=float(np.clip(p_del_hat, 1e-4, 0.95)),
            bg_probs=np.asarray(bg_probs, dtype=np.float64),
            eps_sub=float(np.clip(eps_hat, 0.0, 0.95)),
        )

        # Track (forward) log-likelihood for monitoring
        ll = 0.0
        lik2 = IndelLikelihood(params)
        for o in obs:
            v = lik2.forward_loglik(master, o)
            if not np.isfinite(v):
                ll = -math.inf
                break
            ll += float(v)

        hist['iters'].append(
            {
                'iter': int(it),
                'p_ins': float(params.p_ins),
                'p_del': float(params.p_del),
                'eps_sub': float(params.eps_sub),
                'loglik': float(ll),
            }
        )

    return master, params, hist


def infer_master_workflow(
    sequences: Sequence[TokenSeq],
    *,
    alphabet: Optional[Alphabet] = None,
    # If you supply any of these, the default behavior is to treat them as FIXED (not estimated).
    bg_probs: Optional[np.ndarray] = None,
    p_ins: Optional[float] = None,
    p_del: Optional[float] = None,
    eps_sub: Optional[float] = None,
    fix_provided_params: bool = True,
    # Length control: in your use-case, the default is a *hard cap* at ~12.
    target_master_len: int = 12,
    max_master_len: int = 12,
    lengths: Optional[Sequence[int]] = None,
    # Parameter estimation
    bg_pseudocount: float = 1e-2,
    estimate_bg_from_inserts: bool = True,
    estimate_eps_sub: bool = True,
    eps_max: float = 0.30,
    # Search / robustness
    n_restarts: int = 2,
    em_iters: int = 3,
    grid_search_params: bool = False,
    p_ins_grid: Sequence[float] = (0.02, 0.05, 0.10, 0.20),
    p_del_grid: Sequence[float] = (0.05, 0.10, 0.20, 0.35),
    # Optional MCMC polishing (off by default for large-N regimes)
    do_mcmc_refine: Optional[bool] = None,
    mcmc_steps_refine: int = 800,
    mcmc_temperature: float = 0.7,
    # Scaling controls
    search_subset_size: int = 2000,
    seed: int = 0,
    progress: bool = True,
) -> WorkflowResult:
    """One-call workflow: infer master sequence and (p_ins, p_del, bg, eps_sub).

    Designed defaults for your stated regime:
      - N in [200, 10_000]
      - alphabet size: a few dozen
      - observed lengths: ~ a dozen

    Important default behavior:
      - The inferred master length is capped by ``max_master_len`` (default 12).
        This prevents the workflow from returning long masters even if a naive model
        would assign them high likelihood.

    Inputs
    ------
    sequences:
        A list of observed sequences, each either a string or a list of tokens.

    Returns
    -------
    WorkflowResult with fields:
      - master_tokens: list[str]
      - params: IndelParams
      - score: forward log-likelihood of the final model on *all* sequences
      - diagnostics: details (history, candidate scores, subset sizing)
    """

    if alphabet is None:
        alphabet = Alphabet.from_sequences(sequences)

    # Encode all sequences.
    obs_all = [alphabet.encode(s) for s in sequences]
    N = int(len(obs_all))
    rng = np.random.default_rng(int(seed))

    # Fit an initial background model from all observations (unless bg_probs is provided).
    if bg_probs is None:
        bg_model = BackgroundModel.fit(sequences, alphabet=alphabet, pseudocount=float(bg_pseudocount))
        bg_init = np.asarray(bg_model.probs, dtype=np.float64)
    else:
        bg_init = np.asarray(bg_probs, dtype=np.float64)
        if bg_init.ndim != 1 or bg_init.size != alphabet.size:
            raise ValueError('bg_probs must be a 1D array of length alphabet.size')
        bg_init = bg_init / bg_init.sum()
        bg_model = BackgroundModel(alphabet=alphabet, probs=bg_init)

    # Choose subset for candidate search (keeps runtime stable for large N).
    if N > int(search_subset_size):
        idx = rng.choice(N, size=int(search_subset_size), replace=False)
        obs_search = [obs_all[int(i)] for i in idx]
        sequences_search = [sequences[int(i)] for i in idx]
    else:
        idx = np.arange(N, dtype=np.int32)
        obs_search = obs_all
        sequences_search = sequences

    lens = np.array([int(o.size) for o in obs_all], dtype=np.int32)

    # Candidate master lengths: by default, a small range ending at max_master_len.
    if lengths is None:
        Lmax = int(max_master_len)
        Lmin = max(1, Lmax - 5)
        lengths = list(range(Lmin, Lmax + 1))
    else:
        lengths = [int(L) for L in lengths if 1 <= int(L) <= int(max_master_len)]
        if not lengths:
            raise ValueError("lengths is empty after applying max_master_len cap")

    # Default for MCMC polishing: only do it automatically for smaller N.
    if do_mcmc_refine is None:
        do_mcmc_refine = (N <= 1500)

    diagnostics: Dict[str, Any] = {
        'N': int(N),
        'fixed': {
            'bg_probs': bool(bg_probs is not None and fix_provided_params),
            'p_ins': bool(p_ins is not None and fix_provided_params),
            'p_del': bool(p_del is not None and fix_provided_params),
            'eps_sub': bool(eps_sub is not None and fix_provided_params),
        },
        'search_subset_size_used': int(len(obs_search)),
        'length_candidates': list(lengths),
        'target_master_len': int(target_master_len),
        'max_master_len': int(max_master_len),
        'candidates': [],
    }

    best = {
        'score': -math.inf,
        'master': None,
        'params': None,
        'hist': None,
        'L': None,
    }

    # Optionally do a small parameter grid search (rarely needed with EM, but included).
    if grid_search_params:
        p_ins_list = list(p_ins_grid)
        p_del_list = list(p_del_grid)
    else:
        p_ins_list = [None]
        p_del_list = [None]

    # Outer search loop over lengths (and optionally p-ins/p-del grid)
    outer = [(L, pi, pd) for L in lengths for pi in p_ins_list for pd in p_del_list]
    it_outer = outer
    if progress and _HAVE_TQDM:
        it_outer = tqdm(outer, desc="Workflow search", leave=False)

    for (L, pi, pd) in it_outer:
        L = int(L)

        # Heuristic initial params
        mean_len = float(lens.mean())
        p_del0 = float(p_del) if p_del is not None else (float(pd) if pd is not None else 0.20)
        p_ins0 = float(p_ins) if p_ins is not None else (float(pi) if pi is not None else _estimate_p_ins_from_lengths(mean_len, L, p_del0))
        eps0 = float(eps_sub) if eps_sub is not None else 0.02

        params0 = IndelParams(
            p_ins=float(np.clip(p_ins0, 1e-4, 0.9)),
            p_del=float(np.clip(p_del0, 1e-4, 0.9)),
            bg_probs=np.asarray(bg_model.probs, dtype=np.float64),
            eps_sub=float(eps0),
        )

        # Multiple restarts: different master initializations, take best
        local_best = {'score': -math.inf, 'master': None, 'params': None, 'hist': None}

        for r in range(int(max(1, n_restarts))):
            # init master either from data (closest) or random
            if r == 0:
                init_m = _choose_init_master_from_data(obs_search, target_len=min(L, target_master_len), rng=rng)
            else:
                init_m = rng.integers(0, alphabet.size, size=L, dtype=np.int32)

            # Ensure correct length
            if int(init_m.size) != L:
                init_m = rng.integers(0, alphabet.size, size=L, dtype=np.int32)

            m_em, p_em, hist = _em_fixed_length(
                obs=obs_search,
                master=init_m,
                params=params0,
                n_iters=int(em_iters),
                estimate_bg_from_inserts=bool(estimate_bg_from_inserts),
                estimate_eps_sub=bool(estimate_eps_sub),
                bg_pseudocount=float(bg_pseudocount),
                eps_max=float(eps_max),
                progress=False,
                fixed_p_ins=float(p_ins) if (p_ins is not None and fix_provided_params) else None,
                fixed_p_del=float(p_del) if (p_del is not None and fix_provided_params) else None,
                fixed_eps_sub=float(eps_sub) if (eps_sub is not None and fix_provided_params) else None,
                fixed_bg_probs=bg_init if (bg_probs is not None and fix_provided_params) else None,
            )

            # Score on search subset (forward likelihood, not Viterbi)
            lik = IndelLikelihood(p_em)
            ll = 0.0
            for o in obs_search:
                v = lik.forward_loglik(m_em, o)
                if not np.isfinite(v):
                    ll = -math.inf
                    break
                ll += float(v)

            if float(ll) > float(local_best['score']):
                local_best = {'score': float(ll), 'master': m_em.copy(), 'params': p_em, 'hist': hist}

        diagnostics['candidates'].append(
            {
                'L': int(L),
                'p_ins_init': float(p_ins0),
                'p_del_init': float(p_del0),
                'search_subset_loglik': float(local_best['score']),
            }
        )

        if float(local_best['score']) > float(best['score']):
            best = {
                'score': float(local_best['score']),
                'master': local_best['master'],
                'params': local_best['params'],
                'hist': local_best['hist'],
                'L': int(L),
            }

    assert best['master'] is not None and best['params'] is not None
    master_best = np.asarray(best['master'], dtype=np.int32)
    params_best: IndelParams = best['params']

    # Optional MCMC polish (fixed-length by default to respect max_master_len)
    if bool(do_mcmc_refine):
        est = MasterSequenceEstimator(
            alphabet=alphabet,
            sequences=sequences_search,
            params=params_best,
            seed=int(seed),
        )
        res = est.run_mcmc(
            init_master=master_best,
            n_steps=int(mcmc_steps_refine),
            burn_in=max(100, int(mcmc_steps_refine // 5)),
            thin=max(5, int(mcmc_steps_refine // 100)),
            fixed_length=True,
            temperature=float(mcmc_temperature),
            progress=False,
        )
        master_best = res.best_master.copy()

    # Final parameter refresh on full data (still fixed-length, capped)
    master_final, params_final, hist_full = _em_fixed_length(
        obs=obs_all,
        master=master_best,
        params=params_best,
        n_iters=max(1, int(em_iters)),
        estimate_bg_from_inserts=bool(estimate_bg_from_inserts),
        estimate_eps_sub=bool(estimate_eps_sub),
        bg_pseudocount=float(bg_pseudocount),
        eps_max=float(eps_max),
        progress=False,
        fixed_p_ins=float(p_ins) if (p_ins is not None and fix_provided_params) else None,
        fixed_p_del=float(p_del) if (p_del is not None and fix_provided_params) else None,
        fixed_eps_sub=float(eps_sub) if (eps_sub is not None and fix_provided_params) else None,
        fixed_bg_probs=bg_init if (bg_probs is not None and fix_provided_params) else None,
    )

    # Full-data forward log-likelihood score
    lik_full = IndelLikelihood(params_final)
    full_ll = 0.0
    for o in obs_all:
        v = lik_full.forward_loglik(master_final, o)
        if not np.isfinite(v):
            full_ll = -math.inf
            break
        full_ll += float(v)

    diagnostics['best_length'] = int(master_final.size)
    diagnostics['best_search_subset_loglik'] = float(best['score'])
    diagnostics['em_history_search'] = best['hist']
    diagnostics['em_history_full'] = hist_full
    diagnostics['do_mcmc_refine'] = bool(do_mcmc_refine)

    return WorkflowResult(
        master_tokens=alphabet.decode(master_final),
        params=params_final,
        score=float(full_ll),
        diagnostics=diagnostics,
    )
