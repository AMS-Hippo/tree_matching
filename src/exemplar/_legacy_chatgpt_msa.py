@dataclass
class GridSearchResult:
    best_master: np.ndarray
    best_params: IndelParams
    best_score: float
    all_results: List[Dict[str, Any]]


def grid_search_master(
    sequences: Sequence[TokenSeq],
    alphabet: Optional[Alphabet] = None,
    bg_model: Optional[BackgroundModel] = None,
    lengths: Optional[Sequence[int]] = None,
    p_ins_grid: Sequence[float] = (0.05, 0.1, 0.2),
    p_del_grid: Sequence[float] = (0.05, 0.1, 0.2),
    eps_sub: float = 0.01,
    n_steps: int = 3000,
    seed: int = 0,
    progress: bool = True,
) -> GridSearchResult:
    """Simple robust (but not fast) grid search over (L, p_ins, p_del).

    For each candidate, runs MCMC over the master sequence at fixed length L (substitution moves only).

    If lengths is None, uses a default candidate set based on observed lengths.
    """
    if alphabet is None:
        alphabet = Alphabet.from_sequences(sequences)
    if bg_model is None:
        bg_model = BackgroundModel.fit(sequences, alphabet=alphabet)

    enc_seqs = [alphabet.encode(s) for s in sequences]
    lens = [int(s.size) for s in enc_seqs]
    if lengths is None:
        mn, mx = int(min(lens)), int(max(lens))
        # Reasonable default: a few candidates around the median and below.
        med = int(np.median(lens))
        cand = sorted(set([max(1, med), max(1, med - mn // 4), max(1, med + mn // 4), max(1, mn // 2), mx]))
        lengths = cand

    all_res: List[Dict[str, Any]] = []
    best_score = -math.inf
    best_master = None
    best_params = None

    outer = [(L, pi, pd) for L in lengths for pi in p_ins_grid for pd in p_del_grid]

    it = outer
    if progress and _HAVE_TQDM:
        it = tqdm(outer, desc="Grid search", leave=False)

    for (L, p_ins, p_del) in it:
        params = IndelParams(p_ins=float(p_ins), p_del=float(p_del), bg_probs=bg_model.probs, eps_sub=float(eps_sub))
        est = MasterSequenceEstimator(alphabet=alphabet, sequences=sequences, params=params, seed=seed)

        init = est.init_random(length=int(L))
        res = est.run_mcmc(
            init_master=init,
            n_steps=int(n_steps),
            burn_in=max(100, int(n_steps // 10)),
            thin=max(5, int(n_steps // 100)),
            fixed_length=True,
            temperature=0.7,  # mild annealing
            progress=False,
        )
        all_res.append(
            {
                'L': int(L),
                'p_ins': float(p_ins),
                'p_del': float(p_del),
                'score': float(res.best_score),
                'accept_rate': float(res.accept_rate),
            }
        )
        if float(res.best_score) > best_score:
            best_score = float(res.best_score)
            best_master = res.best_master.copy()
            best_params = params

    assert best_master is not None and best_params is not None
    return GridSearchResult(
        best_master=best_master,
        best_params=best_params,
        best_score=float(best_score),
        all_results=all_res,
    )


