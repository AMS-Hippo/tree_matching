@dataclass
class BackgroundModel:
    """Categorical background distribution over an Alphabet."""

    alphabet: Alphabet
    probs: np.ndarray  # shape (A,)

    @staticmethod
    def fit(
        seqs: Sequence[TokenSeq],
        alphabet: Alphabet,
        pseudocount: float = 1e-2,
    ) -> "BackgroundModel":
        """Fit bg by empirical counts with additive smoothing."""
        counts = np.full(alphabet.size, float(pseudocount), dtype=np.float64)
        for s in seqs:
            enc = alphabet.encode(s)
            if enc.size:
                binc = np.bincount(enc, minlength=alphabet.size).astype(np.float64)
                counts += binc
        probs = counts / counts.sum()
        return BackgroundModel(alphabet=alphabet, probs=probs)

    def sample(self, rng: np.random.Generator, size: int = 1) -> np.ndarray:
        return rng.choice(self.alphabet.size, size=size, p=self.probs)


