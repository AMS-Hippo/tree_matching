def simulate_observation(
    master: np.ndarray,
    params: Union[IndelParams, IndelModelParams],
    rng: np.random.Generator,
) -> np.ndarray:
    """Simulate one observed sequence from a master under the model."""
    master = np.asarray(master, dtype=np.int32)
    A = int(params.bg_probs.size)
    bg = np.asarray(params.bg_probs, dtype=np.float64)
    bg = bg / bg.sum()

    obs: List[int] = []

    def sample_bg() -> int:
        return int(rng.choice(A, p=bg))

    for sym in master.tolist():
        # inserts before this master symbol
        while rng.random() < params.p_ins:
            obs.append(sample_bg())

        # delete or emit
        if rng.random() < params.p_del:
            continue

        if params.eps_sub > 0.0 and rng.random() < params.eps_sub:
            obs.append(sample_bg())
        else:
            obs.append(int(sym))

    # tail inserts
    while rng.random() < params.p_ins:
        obs.append(sample_bg())

    return np.asarray(obs, dtype=np.int32)


def lcs_length(a: Sequence[int], b: Sequence[int]) -> int:
    """Length of longest common subsequence (O(len(a)*len(b)))."""
    n, m = len(a), len(b)
    if n == 0 or m == 0:
        return 0
    # Use two-row DP for memory.
    prev = np.zeros(m + 1, dtype=np.int32)
    cur = np.zeros(m + 1, dtype=np.int32)
    for i in range(1, n + 1):
        ai = a[i - 1]
        for j in range(1, m + 1):
            if ai == b[j - 1]:
                cur[j] = prev[j - 1] + 1
            else:
                cur[j] = cur[j - 1] if cur[j - 1] >= prev[j] else prev[j]
        prev, cur = cur, prev
    return int(prev[m])


def edit_distance(a: Sequence[int], b: Sequence[int]) -> int:
    """Levenshtein edit distance (insert/delete/substitute), O(n*m) time, O(m) memory."""
    n, m = len(a), len(b)
    if n == 0:
        return m
    if m == 0:
        return n
    prev = np.arange(m + 1, dtype=np.int32)
    cur = np.empty(m + 1, dtype=np.int32)
    for i in range(1, n + 1):
        cur[0] = i
        ai = a[i - 1]
        for j in range(1, m + 1):
            cost = 0 if ai == b[j - 1] else 1
            cur[j] = min(
                prev[j] + 1,        # delete
                cur[j - 1] + 1,     # insert
                prev[j - 1] + cost, # substitute
            )
        prev, cur = cur, prev
    return int(prev[m])


def decode_tokens(alphabet: Alphabet, seq: np.ndarray) -> List[str]:
    return alphabet.decode(seq)




def simulate_dataset(
    master_tokens: TokenSeq,
    n_obs: int,
    params: IndelModelParams,
    rng: Optional[np.random.Generator] = None,
) -> List[List[str]]:
    """Simulate a list of observed token sequences.

    Returns a list of token lists (not encoded ints), suitable for BackgroundModel.fit
    and MasterSequenceEstimator.
    """
    if rng is None:
        rng = np.random.default_rng(0)
    master_int = params.alphabet.encode(master_tokens)
    core = params.to_core()
    out: List[List[str]] = []
    for _ in range(int(n_obs)):
        obs_int = simulate_observation(master_int, core, rng)
        out.append(params.alphabet.decode(obs_int))
    return out



